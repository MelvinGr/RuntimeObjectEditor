using System;
using System.Windows.Forms;

namespace RuntimeObjectEditor.Test
{
    /// <summary>
    ///     Out of process loader for the RuntimeObjectEditor. This can be used to start the editor and then inject it into
    ///     other processes.
    /// </summary>
    public class Program
    {
        /// <summary>
        ///     The main entry point for the application.
        /// </summary>
        [STAThread]
        private static void Main()
        {
            Application.EnableVisualStyles();
            Application.Run(ObjectEditor.Instance.CreateEditor());
        }
    }
}